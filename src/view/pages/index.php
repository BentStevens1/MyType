      <p class= "body__text body__text-padding">In 1722 my friend William Bowyer saw my desire, acknowledged my talent and asked me to cut roman, italic, and Hebrew typefaces. A new roman typeface with lots of character. It was first used in 1726 and a few years later it came to be called Caslon. Named after me of course.</p>
      <p class= "body__text body__text-padding">I issued my first official specimen sheet of the typeface in 1734 and exhibited the roman and italic types in 14 different sizes. The Caslon typeface eventually spread all over Europe and the American colonies. It became so popular that the expression “When in doubt, use Caslon” came about. I am really proud of that. </p>
