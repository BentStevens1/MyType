<section>
  <h2>Chapter 4</h2>
  <h1>Changing the world</h1>

  <div>
    <p>Caslon is a versatile typeface that can be found in a wide variety of places. Places where history has been written. My typeface was considered the font of choice for royalty and politics.</p>
    <p>I surpassed all of my competitors in no time and became the exclusive type foundry for the King’s printers. Many great works of literature and documents of historical importance used my Caslon typeface.</p>
    <p>It was used in the first printed version of the United States Declaration of Independence. And George Bernard Shaw asked specifically that all his plays were set in the Caslon typeface. I never met George, but he seems like a nice guy.</p>
  </div>

