<section>
  <h2>Chapter 2</h2>
  <h1>My first love</h1>
  <p>I fell in love with making type punches and was very good at it, if I say so myself. In the years that followed, I took on countless of other jobs for companies that had to do with letter engraving. In 1720 William Bowyer asked me to design an English Arabic for Christian publications to be distributed in the Middle East. The typeface is now used in psalter and the New Testament. William saw the passion in me and we became good friends. Through jobs like this, the word spread about my type engraving.</p>
</section>

<section>
  <h1>With a little help from my friends</h1>
  <p>Although my success with the typefaces in England were almost instantaneous, I did not do this on my own. Banks gave me loans and sufficient money to go on. Having contact with printers, I was induced to start up a type foundry, largely through the encouragement of my dear friend William Bowyer. And off course, then there was my lovely wife Elizabeth who assisted me in getting the foundry reputation going. I met Elizabeth in the first years that I worked in London. </p>
</section>

<section>
  <h2>Chapter 3</h2>
  <h1>Successful, but incomplete</h1>
  <p>Sure there where challenges, moments of doubt and setbacks. But from 1720 to 1780 few books were printed in England that did not use type from my foundry. Yes, I made a lot of typefaces in my years as a type founder. Still I felt a burning desire to keep moving. Why did I leave my simple life and home town? Well, I wanted to have a lasting impact on the world. And now I was ready.</p>
</section>

