<section>
  <h2 class= "chapter">Chapter 4</h2>
  <h1 class= "title">Changing the world</h1>

  <div>
    <p class= "body__text">Caslon is a versatile typeface that can be found in a wide variety of places. Places where history has been written. My typeface was considered the font of choice for royalty and politics.</p>
    <p class= "body__text">I surpassed all of my competitors in no time and became the exclusive type foundry for the King’s printers. Many great works of literature and documents of historical importance used my Caslon typeface.</p>
    <p class= "body__text">It was used in the first printed version of the United States Declaration of Independence. And George Bernard Shaw asked specifically that all his plays were set in the Caslon typeface. I never met George, but he seems like a nice guy.</p>
  </div>

