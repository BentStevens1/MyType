  <div>
    <h2 class= "chapter chapter__1">Chapter 1</h2>
    <h1 class= "title title__life">The simple life</h1>
    <p class= "body__text">It was a cold morning in the year 1692. I was born in a small village named Cradley, Worcestershire. I had a hard working family and from early on I was trained to be an engraver. Engraving is one of the oldest and most important techniques when producing images on paper in artistic printmaking. It was a simple life. Around the age of seventeen, I became an apprentice to a local engraver of gunlocks and barrels. That’s where I started to learn all about the ins and out of metalwork.</p>
  </div>
</section>

<section>
  <h1 class= " title title__call">My call</h1>
  <h2 class= "title title__adventure">to adventure</h2>

  <div>
    <img src="" alt="">
  </div>

  <div>
    <p class= "body__text body__text-padding">In 1716 I started a business in London. I was an official engraver of gun locks and barrels, and made tools for bookbinders. Metalwork is a form of art in my opinion, it requires precision and patience.</p>
    <p class= "body__text body__text-padding">For seven years I did numerous assignments for many different people. My metal craftsmanship was noticed by the printer John Watts. He gave me the task of cutting type punches for various presses in London. I challenged myself and took the job.</p>
    <p class= "body__text body__text-padding">It's from that moment that I became more and more interested in the art of letter engraving.</p>
  </div>
</section>

