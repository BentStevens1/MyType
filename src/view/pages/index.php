<section>
  <h2 class= "chapter">Chapter 5</h2>
  <h class= "title"1>What makes my Caslon type so special, you might ask?</h1>
  <p class= "body__text">The Caslon typeface is extremely legible, yet graceful and classic. That is why a lot of printers like to use it for printed communication pieces. I designed the typeface with the Old-style typeface characteristics in mind. I like the look of the Old-style typefaces because of their greater refinement.</p>
</section>

<section>
  <img src="" alt="">
